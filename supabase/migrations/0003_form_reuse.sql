-- תבנית לשימוש חוזר מול תבנית לשימוש חד-פעמי: בעת יצירת הטופס המנהל בוחר
-- אם הוא ניתן לשימוש חוזר (ברירת מחדל) או לשימוש חד-פעמי בלבד.
-- archived_at: מתמלא אוטומטית כשהגשה על תבנית חד-פעמית מושלמת — מסמן
-- שהתבנית כבר אינה זמינה ליצירת הגשות חדשות, מבלי למחוק אותה (כדי לא
-- לפגוע בתיעוד/הוכחת-החתימה של ההגשות הקיימות, שמקושרות אליה ב-cascade).
alter table forms
  add column is_reusable boolean not null default true,
  add column archived_at timestamptz;
